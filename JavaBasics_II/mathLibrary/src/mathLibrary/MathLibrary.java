package mathLibrary;

public class MathLibrary {

	public static void main(String[] args) {

		System.out.println(Math.class);
		System.out.println(Math.E);
		System.out.println(Math.PI);
		System.out.println(Math.abs(-15));
		System.out.println(Math.abs(18.7));
		System.out.println(Math.acos(0.54));
		System.out.println(Math.acos(1.1));
		System.out.println(Math.addExact(3, 5));
		System.out.println(Math.ceil(2.3));
		System.out.println(Math.floor(2.6));
		System.out.println(Math.decrementExact(9));
		System.out.println(Math.exp(2));
		System.out.println(Math.incrementExact(9));
		System.out.println(Math.random());
		System.out.println(Math.toIntExact((long) 9.3335687));
		System.out.println(Math.log10(12));

	}

}
